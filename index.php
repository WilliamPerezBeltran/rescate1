var slidesChangeColor = ['#quote1','#quote2','#quote3'];

//Establece la distancia del cambio de color del nav
var height_mobile_nav = document.querySelector('.mobile-nav').offsetTop
 +document.querySelector('.mobile-nav').offsetHeight;

// init controller
var controller = new ScrollMagic.Controller();

function pass(){
  // debugger;
                if (typeof(this.triggerElement) != "undefined") { 
                      var height_screen = $(this.triggerElement()).outerHeight();
                    if (document.querySelector('.scrollmagic-pin-spacer')) {
                        var margin_bottom_div_scroll = document.querySelector('.scrollmagic-pin-spacer').style.marginBottom;
                        return $(this.triggerElement()).outerHeight()+parseInt(margin_bottom_div_scroll)

                    }
                    
                    return height_screen;
                }
                return 1;

                
    }

for (var i = 0 ; i < slidesChangeColor.length ; i++) {

// build scene
var scene = new ScrollMagic.Scene({
      triggerElement: slidesChangeColor[i],
      triggerHook: height_mobile_nav / window.innerHeight,
    })
    .addIndicators({name: "1 (duration: 0)"}) // add indicators (requires plugin)
    .addTo(controller); 
    scene.duration(pass);

    

    scene.on("enter", function (event) {
      $( "#nav span" ).addClass( "change-color" );
      $( ".title h2" ).addClass( "change-color-title" );
      console.log("Scene entered.");
    });

    scene.on("leave", function (event) {
      $( "#nav span" ).removeClass( "change-color" );
      $( ".title h2" ).removeClass( "change-color-title" );
      console.log("Scene leave.");
    });

    scene.on("progress", function (event) {
        console.log("Scene progress changed to " + event.progress);
        console.log("Scene progress changed to " + event.state);
    });

}

